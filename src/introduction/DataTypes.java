package introduction;

public class DataTypes {
    public static void main(String[] args) {
        //numeric data types that holds whole numbers
        byte age=25;
        short year=2024;
        int population=320000000;
        long card=123456789123456789L;

        //floating point
        float diameters=3.1f;
        double price=99.99;

        //non numerical variables
        char character='A';
        boolean hungry=false;
        boolean tired=true;
    }
}
